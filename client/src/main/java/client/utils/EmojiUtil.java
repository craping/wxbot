	public static String formatFace(String content){
		if(content == null || content.isEmpty())
			return "";
		for (Map.Entry<String, String> entry : FACES.entrySet()) {
			if(content.contains(entry.getKey())) {
				content = content.replace(entry.getKey(), "<span class='"+(entry.getKey().contains("<")?"emoji emoji":"qqemoji qqemoji")+entry.getValue()+"'></span>");
			}
		}
		return content;
	}
